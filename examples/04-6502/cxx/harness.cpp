// Apple-1 PIA: CS0 = A4。$D000-$D0FF 内でビット4が立ったアドレスはすべて
// 同じ PIA レジスタ (RS = A1:A0) にマッピングされる。
// 例) $D010/$D0F0 = KBD, $D011/$D0F1 = KBDCR, $D012/$D0F2 = DSP, $D013/$D0F3 = DSPCR
static inline bool is_pia(uint16_t addr) {
    return (addr & 0xFF10u) == 0xD010u;
}

static uint8_t memory_read(uint16_t addr) {
    if (is_pia(addr)) {
        switch (addr & 3u) {
            case 0: {
                uint8_t d = kbd_data;
                kbd_data &= 0x7F;  // clear strobe on read
                kbd_cr   &= 0x7F;
                return d;
            }
            case 1: return kbd_cr;
            case 2: return 0x00;  // DSP: always ready (bit7=0)
            case 3: return 0x00;
        }
    }
    return ram[addr];
}

static void memory_write(uint16_t addr, uint8_t data) {
    if (is_pia(addr)) {
        if ((addr & 3u) == 2u) {  // DSP write
            uint8_t ch = data & 0x7F;
            if (ch == 0x0D) ch = 0x0A;  // CR → LF for xterm.js
            if (ch >= 0x20 || ch == 0x0A) {
                int next = (disp_q_tail + 1) & 1023;
                if (next != disp_q_head) {
                    disp_queue[disp_q_tail] = ch;
                    disp_q_tail = next;
                }
                disp_count++;
            }
        }
        return;
    }
    if (addr < 0xE000) {  // RAM only; $E000+ は ROM
        ram[addr] = data;
    }
}

static void load_rom() {
    memcpy(&ram[0xE000], BASIC,  4096);
    memcpy(&ram[0xFF00], WOZMON, 256);
}

static void write_ring() {
    uint32_t idx = (head & (RING_SIZE - 1)) << 1;
    uint8_t  db  = top->o_we ? top->o_do : top->i_di;
    ring[idx]     = (uint32_t)(top->o_ab)
                  | ((uint32_t)db              << 16)
                  | ((uint32_t)(top->o_we    & 1u) << 24)
                  | ((uint32_t)(top->o_sync  & 1u) << 25)
                  | ((uint32_t)(top->reset   & 1u) << 26);
    ring[idx + 1] = (uint32_t)(top->o_a)
                  | ((uint32_t)(top->o_x)  << 8)
                  | ((uint32_t)(top->o_y)  << 16)
                  | ((uint32_t)(top->o_sp) << 24);
    head++;
}

